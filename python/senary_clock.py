def getGlyph(d1, d2, d3):
    if d1 == 0:
        if d2 > 3:
            return 6-d2
        else:
            return d2
    elif d1 == 1 or d1 == 5:
        if d2 == 0:
            return 1
        elif d2 == 1 or d2 == 5:
            if d3 == 2 or d3 == 4:
                return 4
            else:
                return 1
        elif d2 == 2 or d2 == 4:
            if d3 == 3:
                return 5
            else:
                return 4
        else:
            return 5
    elif d1 == 2 or d1 == 4:
        if d2 == 0:
            return 2
        elif d2 == 2 or d2 == 4:
            if d3 == 2 or d3 == 4:
                return 6
            else:
                return 2
        elif d2 == 1 or d2 == 5:
            if d3 == 3:
                return 5
            else:
                return 4
        else:
            return 5
    else:
        if d2 == 0:
            return 3
        elif d2 == 3:
            return 3
        else:
            return 5

